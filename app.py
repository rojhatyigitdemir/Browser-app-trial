    "            # E-posta bulunamadıysa kaydet ve başarılı sayfasını göster (Sadece 1 kez hakkı oluyor)\n",
    "            cursor.execute(\"INSERT INTO kunden (email, newsletter_optin) VALUES (?, ?)\", (email, newsletter_optin))\n",
