    "@app.route(\"/gratis-getraenk\", methods=[\"GET\", \"POST\"])\n",
    "def gratis_getraenk():\n",
    "    if request.method == \"POST\":\n",
    "        email = request.form.get(\"email\")\n",
    "        newsletter_optin = 1 if request.form.get(\"newsletter\") else 0\n",
    "        \n",
    "        conn = sqlite3.connect('restoran.db')\n",
    "        cursor = conn.cursor()\n",
    "        \n",
    "        # E-posta adresinin daha önce kullanılıp kullanılmadığını kontrol et\n",
    "        cursor.execute(\"SELECT * FROM kunden WHERE email = ?\", (email,))\n",
    "        kayit = cursor.fetchone()\n",
    "        \n",
    "        if kayit:\n",
    "            conn.close()\n",
    "            # E-posta bulunduysa hata mesajı göster\n",
